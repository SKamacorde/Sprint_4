CREATE TABLE TBL_CULTURA_PRODUTO_SENSOR_CONFIGURACAO
(
	cd_cultura_produto_sensor_configuracao INT IDENTITY(1,1) PRIMARY KEY,
	cd_cultura_produto_sensor  INT NOT NULL,    
	vlr_minimo                 DECIMAL(10,6) NOT NULL,
	vlr_maximo                 DECIMAL(10,6) NOT NULL,
	cd_usuario_inclusao        INT NOT NULL,
	dt_inclusao                DATETIME2 NOT NULL
);

CREATE TABLE TBL_CULTURA_PRODUTO_SENSOR
(
	cd_cultura_produto_sensor INT IDENTITY(1,1) PRIMARY KEY,
	cd_cultura           INT NOT NULL,
	cd_produto_sensor    INT NOT NULL,   
	cd_usuario_inclusao  INT NOT NULL,
	dt_inclusao          DATETIME2 NOT NULL
);

CREATE TABLE TBL_PRODUTO_SENSOR
(
	cd_produto_sensor    INT IDENTITY(1,1) PRIMARY KEY,
	cd_sensor            INT NOT NULL,
	cd_produto           INT NOT NULL,
	cd_status            INT DEFAULT 1 NOT NULL,
	cd_usuario_inclusao  INT NOT NULL,
	dt_inclusao          DATE NOT NULL,
	cd_usuario_alteracao INT,
	dt_alteracao         DATETIME2
);

CREATE TABLE TBL_CULTURA
(
	cd_cultura           INT IDENTITY(1,1) PRIMARY KEY,
	nm_cultura           VARCHAR(50) NOT NULL,
	cd_status            INT DEFAULT 1 NOT NULL,
	cd_usuario_inclusao  INT NOT NULL,
	dt_inclusao          DATE NOT NULL,
	cd_usuario_alteracao INT,
	dt_alteracao         DATETIME2
);

CREATE TABLE TBL_PRODUTO
(
	cd_produto           INT IDENTITY(1,1) PRIMARY KEY,
	nm_produto           VARCHAR(50) NOT NULL,
	cd_status            INT DEFAULT 1 NOT NULL,
	cd_usuario_inclusao  INT NOT NULL,
	dt_inclusao          DATE NOT NULL,
	cd_usuario_alteracao INT,
	dt_alteracao         DATETIME2
);

CREATE TABLE TBL_SENSOR
(
	cd_sensor            INT IDENTITY(1,1) PRIMARY KEY,
	nm_sensor            VARCHAR(50) NOT NULL,
	cd_status            INT DEFAULT 1 NOT NULL,
	cd_usuario_inclusao  INT NOT NULL,
	dt_inclusao          DATE NOT NULL,
	cd_usuario_alteracao INT,
	dt_alteracao         DATETIME2
);

