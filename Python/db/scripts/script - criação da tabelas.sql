CREATE TABLE TBL_MONITORAMENTO
(
	cd_monitoramento      INT IDENTITY(1,1) PRIMARY KEY,
	cd_cultura_produto_sensor INT NOT NULL,
	vlr_medido            DECIMAL(10, 6) NOT NULL,
	dt_medicao            DATETIME2 NOT NULL,
	cd_usuario_inclusao   INT NOT NULL,
	dt_inclusao           DATETIME2 NOT NULL
