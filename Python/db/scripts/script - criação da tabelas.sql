-- Tabelas
CREATE TABLE TBL_APLICACAO_MONITORAMENTO
(
	cd_aplicacao          INT IDENTITY(1,1) PRIMARY KEY,
	cd_monitoramento      INT NOT NULL,
	vlr_medido            DECIMAL(10, 6) NOT NULL,
	vlr_minimo            DECIMAL(10, 6) NOT NULL,
	vlr_maximo            DECIMAL(10, 6) NOT NULL,
	vlr_aplicado          DECIMAL(10, 6) NOT NULL,	
	cd_usuario_inclusao   INT NOT NULL,
	dt_inclusao           DATETIME2 NOT NULL
